package pathfinder.item;

public class Armor {
    int armorClassIncrease;
    int dexLimit;

    public int getArmorClassIncrease() {
        return armorClassIncrease;
    }

    public void setArmorClassIncrease(int armorClassIncrease) {
        this.armorClassIncrease = armorClassIncrease;
    }

    public int getDexLimit() {
        return dexLimit;
    }

    public void setDexLimit(int dexLimit) {
        this.dexLimit = dexLimit;
    }
}
